#include "../includes/libft.h"

size_t		ft_intlen(intmax_t n)
{
	size_t	len;

	len = (n <= 0 ? 1 : 0);
	if (n < 0)
		n = -n;
	while (n != 0)
	{
		n /= 10;
		++len;
	}
	return (len);
}
